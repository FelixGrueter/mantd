/* ============================================================
 *  setLanguage(lang)
 *  Switches all data-i18n elements, updates <html lang>,
 *  <title>, <meta description>, localStorage and switcher UI.
 * ============================================================ */
function setLanguage(lang) {
  if (!translations[lang]) return;

  const t = translations[lang];

  // Fade out
  document.body.style.transition = 'opacity 0.18s ease';
  document.body.style.opacity    = '0';

  setTimeout(() => {
    // Update every [data-i18n] element
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) {
        // Use innerHTML for elements that may contain HTML tags
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = t[key];
        } else {
          el.textContent = t[key];
        }
      }
    });

    // Update [data-i18n-placeholder]
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (t[key] !== undefined) el.placeholder = t[key];
    });

    // Update [data-i18n-alt] on images
    document.querySelectorAll('[data-i18n-alt]').forEach(el => {
      const key = el.getAttribute('data-i18n-alt');
      if (t[key] !== undefined) el.alt = t[key];
    });

    // Update [data-i18n-aria] for aria-labels
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
      const key = el.getAttribute('data-i18n-aria');
      if (t[key] !== undefined) el.setAttribute('aria-label', t[key]);
    });

    // Update contact form success alert
    const form = document.querySelector('.kontakt__form');
    if (form) {
      form.setAttribute('data-success-msg', t['kontakt.success'] || '');
    }

    // Meta title & description
    if (t['meta.title'])       document.title = t['meta.title'];
    if (t['meta.description']) {
      const metaDesc = document.querySelector('meta[name="description"]');
      if (metaDesc) metaDesc.setAttribute('content', t['meta.description']);
    }

    // <html lang>
    document.documentElement.lang = lang;

    // Update switcher active state
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
      btn.setAttribute('aria-pressed', btn.dataset.lang === lang ? 'true' : 'false');
    });

    // Persist
    localStorage.setItem('mantd-lang', lang);

    // Fade in
    document.body.style.opacity = '1';
  }, 180);
}

/* ── Auto-init on DOM ready ── */
document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('mantd-lang') || 'de';
  setLanguage(saved);
});
